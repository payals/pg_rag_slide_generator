class TestStripCitationHashes:
    """Tests for _strip_citation_hashes helper."""

    def test_removes_single_hash(self):
        result = _strip_citation_hashes(["pgvector does X. [7145eea4]"])
        assert result == ["pgvector does X."]

    def test_removes_multiple_hashes(self):
        result = _strip_citation_hashes(["hybrid search. [bfbd1b21][8e24e3a6]"])
        assert result == ["hybrid search."]

    def test_no_hashes_unchanged(self):
        result = _strip_citation_hashes(["plain bullet with no hash"])
        assert result == ["plain bullet with no hash"]

    def test_empty_list(self):
        assert _strip_citation_hashes([]) == []

    def test_preserves_non_hex_brackets(self):
        result = _strip_citation_hashes(["see [section 1] for details"])
        assert result == ["see [section 1] for details"]

    def test_removes_full_uuid(self):
        result = _strip_citation_hashes(
            ["MCP typed tools. [ad77fde7-a7d3-4a75-80ac-0c62cbb08c09]"]
        )
        assert result == ["MCP typed tools."]

    def test_removes_comma_separated_uuids(self):
        result = _strip_citation_hashes(
            ["Reusable blocks. [ad77fde7-a7d3-4a75-80ac-0c62cbb08c09,4384ed52-1f04-481d-a183-b1477667a684]"]
        )
        assert result == ["Reusable blocks."]

    def test_removes_comma_separated_uuids_with_spaces(self):
        result = _strip_citation_hashes(
            ["Grounding. [9d77e47b-8115-4c9d-a636-c0a17faa0f91, 4384ed52-1f04-481d-a183-b1477667a684]"]
        )
        assert result == ["Grounding."]

    def test_removes_mixed_short_and_full(self):
        result = _strip_citation_hashes(
            ["First. [7145eea4]", "Second. [ad77fde7-a7d3-4a75-80ac-0c62cbb08c09]"]
        )
        assert result == ["First.", "Second."]


