class TestInjectStaticSlides:
    """Tests for static slide injection."""
    
    def test_adds_title_slide(self, sample_slides):
        """Title slide is added at the beginning."""
        result = inject_static_slides(sample_slides)
        
        assert result[0].is_title is True
        assert result[0].slide_no == 1
        assert result[0].intent == "title"
        assert "Postgres" in result[0].title
    
    def test_adds_thanks_slide(self, sample_slides):
        """Thanks slide is added at the end."""
        result = inject_static_slides(sample_slides)
        
        last = result[-1]
        assert last.is_thanks is True
        assert last.intent == "thanks"
        assert "Thank" in last.title
    
    def test_adds_section_dividers(self, sample_slides):
        """Section dividers are inserted at correct positions."""
        result = inject_static_slides(sample_slides)
        
        dividers = [s for s in result if s.is_divider]
        # Should have divider after "problem" (in sample_slides)
        assert len(dividers) >= 1
        
        # First divider should be "Why Postgres?"
        divider_titles = [d.title for d in dividers]
        assert "Why Postgres?" in divider_titles
    
    def test_preserves_content_slides(self, sample_slides):
        """Original content slides are preserved."""
        result = inject_static_slides(sample_slides)
        
        content_slides = [s for s in result if not s.is_static and not s.is_divider]
        
        assert len(content_slides) == len(sample_slides)
        
        # Check intents are preserved
        result_intents = {s.intent for s in content_slides}
        expected_intents = {s["intent"] for s in sample_slides}
        assert result_intents == expected_intents
    
    def test_sequential_slide_numbers(self, sample_slides):
        """Slide numbers are sequential starting from 1."""
        result = inject_static_slides(sample_slides)
        
        numbers = [s.slide_no for s in result]
        assert numbers == list(range(1, len(result) + 1))
    
    def test_empty_slides_still_adds_static(self):
        """Empty slide list still adds title and thanks."""
        result = inject_static_slides([])
        
        assert len(result) >= 2  # At least title + thanks
        assert result[0].is_title is True
        assert result[-1].is_thanks is True
    
    def test_full_deck_structure(self, full_slide_set):
        """Complete deck has correct structure with all dividers."""
        result = inject_static_slides(full_slide_set)
        
        # Should have 5 section dividers
        dividers = [s for s in result if s.is_divider]
        assert len(dividers) == 5
        
        # Check divider positions
        divider_titles = [d.title for d in dividers]
        for _, title in get_section_dividers():
            assert title in divider_titles

    def test_inserts_static_gates_slide(self, sample_slides):
        """Non-generatable gates intent is inserted as a static slide with image."""
        result = inject_static_slides(sample_slides)
        gates_slides = [s for s in result if s.intent == "gates"]
        assert len(gates_slides) == 1
        gate = gates_slides[0]
        assert gate.is_static is True
        assert gate.title == "Control Gates & Validation"
        assert gate.image_path == "gate-chain-diagram.png"
        assert gate.slide_type == "diagram"

    def test_static_gates_between_mcp_tools_and_observability(self):
        """Gates static slide appears between mcp-tools and observability."""
        slides = [
            {"intent": "mcp-tools", "title": "MCP", "bullets": ["A"], "slide_type": "code",
             "content_data": None, "citations": [], "speaker_notes": "n"},
            {"intent": "observability", "title": "Obs", "bullets": ["B"], "slide_type": "code",
             "content_data": None, "citations": [], "speaker_notes": "n"},
        ]
        result = inject_static_slides(slides)
        intents = [s.intent for s in result if s.intent not in ("title", "thanks", "divider")]
        assert intents == ["mcp-tools", "gates", "observability"]
    
    def test_handles_jsonb_bullets(self):
        """Handles bullets that might be JSON strings from DB."""
        import json
        slides = [{
            "slide_id": str(uuid4()),
            "slide_no": 2,
            "intent": "problem",
            "title": "Test",
            "bullets": json.dumps(["A", "B", "C"]),  # JSON string
            "speaker_notes": "Notes",
            "citations": json.dumps([]),
        }]
        
        result = inject_static_slides(slides)
        content_slide = [s for s in result if s.intent == "problem"][0]
        
        assert content_slide.bullets == ["A", "B", "C"]
    
    def test_normalizes_doc_title_citations(self):
        """Citations with doc_title key are normalized to include title."""
        slides = [{
            "slide_id": str(uuid4()),
            "slide_no": 2,
            "intent": "problem",
            "title": "Test",
            "bullets": ["A", "B", "C"],
            "speaker_notes": "Notes",
            "citations": [{"chunk_id": "uuid-1", "doc_title": "RAG Overview", "relevance": "..."}],
        }]
        
        result = inject_static_slides(slides)
        content_slide = [s for s in result if s.intent == "problem"][0]
        
        # Citation should now have both doc_title and title
        assert content_slide.citations[0].get("title") == "RAG Overview"
        assert content_slide.citations[0].get("doc_title") == "RAG Overview"

    def test_divider_slides_have_image_path(self, full_slide_set):
        """Divider slides from inject_static_slides have image_path set."""
        result = inject_static_slides(full_slide_set)
        dividers = [s for s in result if s.is_divider]
        divider_images = get_divider_images()

        assert len(dividers) == 5
        for d in dividers:
            assert d.image_path is not None, f"Divider '{d.title}' missing image_path"
            assert d.image_alt is not None, f"Divider '{d.title}' missing image_alt"
            assert d.image_path == divider_images[d.title]

    def test_divider_image_paths_are_filenames(self, full_slide_set):
        """Divider image_path values are bare filenames (not full paths)."""
        result = inject_static_slides(full_slide_set)
        dividers = [s for s in result if s.is_divider]

        for d in dividers:
            assert "/" not in d.image_path, f"Divider image_path should be filename only: {d.image_path}"


